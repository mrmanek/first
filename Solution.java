import java.util.*;

public class Solution {

    public static void main(String[] args) {
        int myInt;
        String myString;
        Scanner scan = new Scanner(System.in);
        myString = scan.nextLine();
        myInt = scan.nextInt();
        scan.close();

        System.out.println("myString is: " + myString);
        System.out.println("myInt is: " + myInt);
}
}